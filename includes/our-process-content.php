                <p>We strive to minimize the UAT (User Acceptance Testing) phase by following the latest industry quality assurance standards in anything we do. If we'd name all the testing standards we aspire to follow, the list would be a size of this page (already unacceptably large). So here are the main testing principles we ensure our team understands and follows:</p>
                <p class="tm-increased tm-colored"><strong>Unit Testing</strong></p>
                <p>Unit testing follows white box testing approach where a developer tests units of source code like statements, branches, functions, methods, etc.</p>
                <p class="tm-increased tm-colored"><strong>Load Testing</strong></p>
                <p>Load testing intends to find bottlenecks or issues that prevent software from performing as intended at its peak workloads.</p>
                <p class="tm-increased tm-colored"><strong>Penetration Testing</strong></p>
                <p>Penetration Testing is a way of ethical hacking. An experienced Penetration tester will use the same methods and tools that a hacker would use with the intention to identify vulnerabilities and get them fixed before a real hacker or a malicious program exploits it.</p>
                <p class="tm-increased tm-colored"><strong>Scalability Testing</strong></p>
                <p>The objective of scalability testing is to test the ability of the software to scale up with increased users, increased transactions, increase in database size etc. It is not necessary for the software performance to increase with the increase in hardware configuration. Scalability tests help to find out how much more workload the software can support with the expansion of userbase, transactions, and data.</p>
